package chapters.chapter3;

// հաշվել շրջանի մակերեսը
public class Area {
    public static void main(String args[]) {
        double pi, r, a;
        r = 10.8; // շրջանագծի շառավիղը
        pi = 3.1416;
        a = pi * r * r;
        System.out.println("շրջանի մակերեսը հավասար է " + a
        );
    }
}
